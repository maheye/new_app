//double > float > long > ubt > short > byte

public class primitivedDataType {
    public static void main(String[] args){
        //byte 
        byte b = 100; 
        System.out.println(b);
        //short
        short c = 200; 
        System.out.println(c);
        //int
        int d = 300; 
        System.out.println(d);
        //long need a letter "L or l" at the end of the number
        long n = 9999999999L;
        System.out.println(n);


        //float need a letter "f or F" at the end of the number
        float f = 10.1f;
        System.out.println(f);
        //double 
        double e = 20.2; 
        System.out.println(e);

        //char

        char z = 'z';
        System.out.println(z);

        //boolean
        boolean o = true;
        System.out.println(o);
    }
}
